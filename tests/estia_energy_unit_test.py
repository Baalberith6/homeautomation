import unittest

from estia_energy import calculate_cop


class TestEstiaEnergy(unittest.TestCase):

    def test(self):
        self.assertEqual((0,0), calculate_cop([0, 0, 0], [18,18,18]))
        self.assertEqual((2, 1287), calculate_cop([1287], [0])) # 18 * 143 = 2574, 2574 / 1287 = 2
        self.assertEqual((2.6153846153846154, 1859), calculate_cop([1287, 572], [0, 2]))  # 2574 + (16 * 143) / 1859 = 2.615
        self.assertEqual((3, 2002), calculate_cop([1287, 572, 143], [0, 2, 10]))  # 2574 + 2288 + (8 * 143) / 2002 = 3
        self.assertEqual((0.468, 11000), calculate_cop([1000, 10000], [0, 0]))
        self.assertEqual((2, 1287), calculate_cop([0, 1287], [18, 0])) # 18 * 143 = 2574, 2574 / 1287 = 2
        self.assertEqual((4.048608310262746, 12920.341013824886), calculate_cop([577, 1122, 26, 902, 1138, 411, 551, 1004, 119, 805, 595, 1046, 1042, 178, 266, 526, 195, 657, 1526, 25, 573, 589, 143, 871], [0.7, 0.7, 1, 0.9, 1.7, 1.7, 1.7, 1.9, 2.2, 2.9, 3.7, 4.4, 5, 4.6, 4.9, 4.2, 3.9, 3.7, 3.4, 3.2, 2.8, 2.4, 2.2, 2.4])) # 1.1.2024
        self.assertEqual((3.3694026682978593, 24246.404494382023), calculate_cop([1111, 1244, 1564, 1490, 1512, 1430, 1418, 1407, 1494, 1262, 167, 1379, 1841, 514, 26, 776, 340, 267, 1692, 1171, 1126, 1216, 1171, 1130], [-10, -10.7, -10.8, -10.6, -11.1, -10.8, -10.5, -10.1, -5.8, -2.7, -2.3, -1.1, -0.5, -0.1, -0.5, -1.2, -2.2, -2.8, -4.4, -5.7, -6.8, -6.9, -6.5, -5.2])) # 21.1.2024
if __name__ == '__main__':
    unittest.main()
